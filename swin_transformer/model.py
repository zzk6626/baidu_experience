class Mlp(nn.Module):
    """ MLP as used in Vision Transformer, MLP-Mixer and related networks
    """
    def __init__(self, in_features, hidden_features=None, out_features=None, act_layer=nn.GELU, drop=0.):
        super().__init__()
        out_features = out_features or in_features
        hidden_features = hidden_features or in_features

        self.fc1 = nn.Linear(in_features, hidden_features)
        self.act = act_layer()
        self.drop1 = nn.Dropout(drop)
        self.fc2 = nn.Linear(hidden_features, out_features)
        self.drop2 = nn.Dropout(drop)

    def forward(self, x):
        x = self.fc1(x)
        x = self.act(x)
        x = self.drop1(x)
        x = self.fc2(x)
        x = self.drop2(x)
        return x
    
# Multi-head Attention相对于Attention的计算量仅仅只是多了最后的一个W_o的计算量
# multi-head self-attention         MSA 并行化 == 矩阵乘法实现
# 每个像素点都求解qkv，然后和像素点的每个k进行匹配计算相似度，匹配的结果与对应k的v相乘，最后相加获得单个像素点的输出 输入shape=输出shape
# Window Multi-head self-attention WMSA 
# 对每个window内部计算mult-head self-attention
# 优点：减少计算量   缺点：感受野减小了，window之间无法信息交互

# Shifted-Window Mulit-head Self-attention 
# 功能：增强window之间的信息交互
# 1 - 调整切割位置，模块大小不一
# 2 - 调整window位置，左右/上下移动，模块组合后的window
# 3 - 带蒙板mask的MSA，通过设置蒙板来隔绝不同区域的信息
# 4 - 分别计算一个window内若干个不连续区域的attention
# 5 - 在计算完后还要把数据给挪回到原来的位置上
'''
    shift-window-attention MASK操作,避免原图中未相邻的图像部分进行信息融合msa计算的话存在问题。
    因此要分开计算。区域3区域5在一个window,但是它们在原图中不是相邻的。window内 计算QK矩阵相乘操作后
    将乘后的p设置为-100,经过softmax后,区域3的权重都为0。 
    mask msa 计算量和之前的msa计算量是相同的,多了一个 -100 的操作）
'''
class WindowAttention(nn.Module):
    r""" Window based multi-head self attention (W-MSA) module with relative position bias.
    It supports both of shifted and non-shifted window.

    Args:
        dim (int): Number of input channels.
        window_size (tuple[int]): The height and width of the window.
        num_heads (int): Number of attention heads.
        qkv_bias (bool, optional):  If True, add a learnable bias to query, key, value. Default: True
        attn_drop (float, optional): Dropout ratio of attention weight. Default: 0.0
        proj_drop (float, optional): Dropout ratio of output. Default: 0.0
    """

    def __init__(self, dim, window_size, num_heads, qkv_bias=True, attn_drop=0., proj_drop=0.):
        '''
        新增内容： 相对位置偏置B => attn = Softmax(Q * KT / sqrt(d) + B) * V
        绝对位置索引 -> 相对位置索引 -> 行列加M-1,行标乘2M-1,行标列标相加 -> 构建相对位置偏置对应表 
        '''
        super().__init__()
        self.dim = dim
        self.window_size = window_size  # [Mh, Mw]
        self.num_heads = num_heads
        head_dim = dim // num_heads
        self.scale = head_dim ** -0.5
        '''
        相对位置索引编码(-1, 0)  经过变换后，其参数值相同。但是同一行(相对位置编码展开),不容许存在相同的位置编码信息(相加操作)
        为什么范围是(2M-1)^2?  因为若尺度为2,相对位置行列索引范围[-M+1, M-1],行(2M-1)列(2M-1)因此relative table的范围是(2M-1)^2
        '''
        # define a parameter table of relative position bias
        # [2*Mh-1 * 2*Mw-1, nH] 针对每一个head，它采用的relative_position_bias_table不一样
        self.relative_position_bias_table = nn.Parameter(
            torch.zeros((2 * window_size[0] - 1) * (2 * window_size[1] - 1), num_heads))  

        # 针对feature_map构建一个绝对索引，若window_size=2，生成[[0,0,1,1],[0,1,0,1]]
        # get pair-wise relative position index for each token inside the window
        coords_h = torch.arange(self.window_size[0])
        coords_w = torch.arange(self.window_size[1])
        coords = torch.stack(torch.meshgrid([coords_h, coords_w], indexing="ij"))  # [2, Mh, Mw]
        coords_flatten = torch.flatten(coords, 1)  # [2, Mh*Mw]

        '''
        相对位置索引 由绝对位置索引变换而来 若A(0,0) B(1,0) 那么若以A作为相对原点时，B的相对位置索引(A-B)
        即相对位置索引指标为 (0-1, 0-0) -> (-1,0)
        '''
        
        # [2, Mh*Mw, 1] - [2, 1, Mh*Mw] boardcast机制，构建了相对位置索引矩阵
        # [[[0,0,-1,-1], ...] , [[0,-1,0,-1],...]] 左右配对来识别相对位置索引
        relative_coords = coords_flatten[:, :, None] - coords_flatten[:, None, :]  # [2, Mh*Mw, Mh*Mw]
        
        '''
        1 - 行标、列标均加上M-1   2 -  行标乘以2M-1      3 - 行标列标相加
        对应的相对位置索引相同的，计算出来的结果仍然相同。 同一行信息(之前的一块)不存在相同的计算结果  <方式优于行列直接相加>
        '''
        # relative_coords [Mh*Mw, Mh*Mw, 2] 以不同点为0,0绝对位置的相对位置索引[[[0,0],[0,-1],[-1,0],[-1,-1]],...]]]        relative_coords = relative_coords.permute(1, 2, 0).contiguous()  # [Mh*Mw, Mh*Mw, 2]
        relative_coords[:, :, 0] += self.window_size[0] - 1  # shift to start from 0
        relative_coords[:, :, 1] += self.window_size[1] - 1
        relative_coords[:, :, 0] *= 2 * self.window_size[1] - 1
        relative_position_index = relative_coords.sum(-1)  # [Mh*Mw, Mh*Mw]
        
        # register_buffer方法，将relative_position_index放入模型的缓存区，后续无需改变
        # 能够被训练的数据是relative_position_bias_table
        self.register_buffer("relative_position_index", relative_position_index)

        self.qkv = nn.Linear(dim, dim * 3, bias=qkv_bias)
        self.attn_drop = nn.Dropout(attn_drop)
        self.proj = nn.Linear(dim, dim)
        self.proj_drop = nn.Dropout(proj_drop)

        nn.init.trunc_normal_(self.relative_position_bias_table, std=.02)
        self.softmax = nn.Softmax(dim=-1)

    def forward(self, x, mask: Optional[torch.Tensor] = None):
        """
        Args:
            x: input features with shape of (num_windows*B, Mh*Mw, C)
            mask: (0/-inf) mask with shape of (num_windows, Wh*Ww, Wh*Ww) or None
        """
        # [batch_size*num_windows, Mh*Mw, total_embed_dim]
        B_, N, C = x.shape
        # qkv(): -> [batch_size*num_windows, Mh*Mw, 3 * total_embed_dim]
        # reshape: -> [batch_size*num_windows, Mh*Mw, 3, num_heads, embed_dim_per_head]
        # permute: -> [3, batch_size*num_windows, num_heads, Mh*Mw, embed_dim_per_head]
        qkv = self.qkv(x).reshape(B_, N, 3, self.num_heads, C // self.num_heads).permute(2, 0, 3, 1, 4)
        # [batch_size*num_windows, num_heads, Mh*Mw, embed_dim_per_head]
        # torch.unbind() 解绑变量，完成某一个维度的切片操作
        q, k, v = qkv.unbind(0)  # make torchscript happy (cannot use tensor as tuple)

        # transpose: -> [batch_size*num_windows, num_heads, embed_dim_per_head, Mh*Mw]
        # @: multiply -> [batch_size*num_windows, num_heads, Mh*Mw, Mh*Mw]
        q = q * self.scale
        attn = (q @ k.transpose(-2, -1))

        # relative_position_bias_table.view: [Mh*Mw*Mh*Mw,nH] -> [Mh*Mw,Mh*Mw,nH]
        # relative_position_index被展平，然后在relative_position_bias_table中取对应的参数
        relative_position_bias = self.relative_position_bias_table[self.relative_position_index.view(-1)].view(
            self.window_size[0] * self.window_size[1], self.window_size[0] * self.window_size[1], -1)
        relative_position_bias = relative_position_bias.permute(2, 0, 1).contiguous()  # [nH, Mh*Mw, Mh*Mw]
        # relative_position_bias 对应公式里面的B，缺少第一个维度，unsqueeze添加一个维度，boardcast机制完成相加操作
        attn = attn + relative_position_bias.unsqueeze(0)

        if mask is not None:
            # mask: [nW, Mh*Mw, Mh*Mw]
            nW = mask.shape[0]  # num_windows
            # attn.view: [batch_size, num_windows, num_heads, Mh*Mw, Mh*Mw]
            # mask.unsqueeze: [1, nW, 1, Mh*Mw, Mh*Mw]
            
            # mask和attn相加，不同区域的mask值为-100。那么其结果为-99，最终softmax后该位置权重为0
            attn = attn.view(B_ // nW, nW, self.num_heads, N, N) + mask.unsqueeze(1).unsqueeze(0)
            attn = attn.view(-1, self.num_heads, N, N)
            attn = self.softmax(attn)
        else:
            attn = self.softmax(attn)

        attn = self.attn_drop(attn)

        # @: multiply -> [batch_size*num_windows, num_heads, Mh*Mw, embed_dim_per_head]
        # transpose: -> [batch_size*num_windows, Mh*Mw, num_heads, embed_dim_per_head]
        # reshape: -> [batch_size*num_windows, Mh*Mw, total_embed_dim]
        x = (attn @ v).transpose(1, 2).reshape(B_, N, C)
        x = self.proj(x)
        x = self.proj_drop(x)
        return x

# SwimTransformerBlock不会改变图片输入的高和宽
# W-MSA SW-MSA 一般都是成对使用的，即depth必须是2的倍数
class SwinTransformerBlock(nn.Module):
    r""" Swin Transformer Block.

    Args:
        dim (int): Number of input channels.
        num_heads (int): Number of attention heads.
        window_size (int): Window size.
        shift_size (int): Shift size for SW-MSA.
        mlp_ratio (float): Ratio of mlp hidden dim to embedding dim.
        qkv_bias (bool, optional): If True, add a learnable bias to query, key, value. Default: True
        drop (float, optional): Dropout rate. Default: 0.0
        attn_drop (float, optional): Attention dropout rate. Default: 0.0
        drop_path (float, optional): Stochastic depth rate. Default: 0.0
        act_layer (nn.Module, optional): Activation layer. Default: nn.GELU
        norm_layer (nn.Module, optional): Normalization layer.  Default: nn.LayerNorm
    """

    def __init__(self, dim, num_heads, window_size=7, shift_size=0,
                 mlp_ratio=4., qkv_bias=True, drop=0., attn_drop=0., drop_path=0.,
                 act_layer=nn.GELU, norm_layer=nn.LayerNorm):
        super().__init__()
        self.dim = dim
        self.num_heads = num_heads
        self.window_size = window_size
        self.shift_size = shift_size
        self.mlp_ratio = mlp_ratio
        # assert - 断言函数，不满足要求时，会报错后面内容
        assert 0 <= self.shift_size < self.window_size, "shift_size must in 0-window_size"

        self.norm1 = norm_layer(dim)
        self.attn = WindowAttention(
            dim, window_size=(self.window_size, self.window_size), num_heads=num_heads, qkv_bias=qkv_bias,
            attn_drop=attn_drop, proj_drop=drop)

        self.drop_path = DropPath(drop_path) if drop_path > 0. else nn.Identity()
        self.norm2 = norm_layer(dim)
        mlp_hidden_dim = int(dim * mlp_ratio)
        # Mlp => FC1 + ACT + DROP1 + FC2 + DROP2
        self.mlp = Mlp(in_features=dim, hidden_features=mlp_hidden_dim, act_layer=act_layer, drop=drop)

    def forward(self, x, attn_mask):
        H, W = self.H, self.W  # 在BasicLayer当中记录了H，W
        B, L, C = x.shape
        assert L == H * W, "input feature has wrong size"

        shortcut = x
        x = self.norm1(x)
        x = x.view(B, H, W, C)  # 重置了维度

        # pad feature maps to multiples of window size
        # 把feature map给pad到window size的整数倍
        pad_l = pad_t = 0
        pad_r = (self.window_size - W % self.window_size) % self.window_size
        pad_b = (self.window_size - H % self.window_size) % self.window_size
        x = F.pad(x, (0, 0, pad_l, pad_r, pad_t, pad_b))
        # Hp,Wp为padding之后新的高度和宽度
        _, Hp, Wp, _ = x.shape

        # self.shift_size>0 -> SW-MSA self.shift_size > 0 => SWMSA
        #                             self.shift_size = 0 => WMSA
        if self.shift_size > 0:
            # dim=1,2 对应 H,W方向
            # shifts = (-shift_size)  从上往下移动 / 从左往右移动shift_size
            shifted_x = torch.roll(x, shifts=(-self.shift_size, -self.shift_size), dims=(1, 2))
        else:
            shifted_x = x
            attn_mask = None

        # partition windows
        x_windows = window_partition(shifted_x, self.window_size)  # [nW*B, Mh, Mw, C]
        x_windows = x_windows.view(-1, self.window_size * self.window_size, C)  # [nW*B, Mh*Mw, C]

        # W-MSA/SW-MSA
        attn_windows = self.attn(x_windows, mask=attn_mask)  # [nW*B, Mh*Mw, C]

        # Merge windows 合并窗口 其中, Hp,Wp为padding之后新的高度和宽度
        attn_windows = attn_windows.view(-1, self.window_size, self.window_size, C)  # [nW*B, Mh, Mw, C]
        shifted_x = window_reverse(attn_windows, self.window_size, Hp, Wp)  # [B, H', W', C]

        # self.shift_size>0 -> SW-MSA -> 还原原始位置 从下往上，从右往左移动shiftsize
        if self.shift_size > 0:
            x = torch.roll(shifted_x, shifts=(self.shift_size, self.shift_size), dims=(1, 2))
        else:
            x = shifted_x
        
        # 若先前padding过，抹除padding的数据
        if pad_r > 0 or pad_b > 0:
            # 把前面pad的数据移除掉
            x = x[:, :H, :W, :].contiguous()

        x = x.view(B, H * W, C)

        # FFN
        x = shortcut + self.drop_path(x)
        x = x + self.drop_path(self.mlp(self.norm2(x)))

        return x


class BasicLayer(nn.Module):
    """
    实现每个stage的功能
    A basic Swin Transformer layer for one stage.

    Args:
        dim (int): Number of input channels.
        depth (int): Number of blocks.
        num_heads (int): Number of attention heads.
        window_size (int): Local window size.
        mlp_ratio (float): Ratio of mlp hidden dim to embedding dim.
        qkv_bias (bool, optional): If True, add a learnable bias to query, key, value. Default: True
        drop (float, optional): Dropout rate. Default: 0.0
        attn_drop (float, optional): Attention dropout rate. Default: 0.0
        drop_path (float | tuple[float], optional): Stochastic depth rate. Default: 0.0
        norm_layer (nn.Module, optional): Normalization layer. Default: nn.LayerNorm
        downsample (nn.Module | None, optional): Downsample layer at the end of the layer. Default: None
        use_checkpoint (bool): Whether to use checkpointing to save memory. Default: False.
    """

    def __init__(self, dim, depth, num_heads, window_size,
                 mlp_ratio=4., qkv_bias=True, drop=0., attn_drop=0.,
                 drop_path=0., norm_layer=nn.LayerNorm, downsample=None, use_checkpoint=False):
        super().__init__()
        self.dim = dim
        self.depth = depth
        self.window_size = window_size
        self.use_checkpoint = use_checkpoint
        self.shift_size = window_size // 2  # shift的size为 窗口大小/2 向下取整

        # self.blocks存储一个stage当中的所有layer
        self.blocks = nn.ModuleList([
            SwinTransformerBlock(
                dim=dim,
                num_heads=num_heads,
                window_size=window_size,
                # 通过i是否整除2来判断，当前的layer为MSA / WMSA 
                # W-MSA + SW-MSA + ... + W-MSA + SW-MSA
                shift_size=0 if (i % 2 == 0) else self.shift_size,
                mlp_ratio=mlp_ratio,
                qkv_bias=qkv_bias,
                drop=drop,
                attn_drop=attn_drop,
                drop_path=drop_path[i] if isinstance(drop_path, list) else drop_path,
                norm_layer=norm_layer)
            for i in range(depth)])   # depth表示当前stage存在depth个block

        # patch merging layer
        if downsample is not None:
            # PatchMerging实例化
            self.downsample = downsample(dim=dim, norm_layer=norm_layer)
        else:
            self.downsample = None
    
    # create_mask 若为SW-MSA时 创建蒙板
    # 根据不同尺寸的x,可以重新生成mask蒙板，解决多尺度问题
    # (区别于源码，根据输入直接固定了mask，若后续尺寸不一样会导致报错)
    def create_mask(self, x, H, W):
        # calculate attention mask for SW-MSA
        # 必须保证蒙版的Hp和Wp是window_size的整数倍,支持多尺度创建的
        Hp = int(np.ceil(H / self.window_size)) * self.window_size  # ceil向上取整
        Wp = int(np.ceil(W / self.window_size)) * self.window_size
        # 拥有和feature map一样的通道排列顺序，方便后续window_partition
        img_mask = torch.zeros((1, Hp, Wp, 1), device=x.device)  # [1, Hp, Wp, 1]
        h_slices = (slice(0, -self.window_size),  # slice切片，左闭右开 
                    slice(-self.window_size, -self.shift_size),
                    slice(-self.shift_size, None))
        w_slices = (slice(0, -self.window_size),
                    slice(-self.window_size, -self.shift_size),
                    slice(-self.shift_size, None))
        cnt = 0
        # 划分区域后，相同的cnt代表区域是连续的
        for h in h_slices:
            for w in w_slices:
                img_mask[:, h, w, :] = cnt
                cnt += 1
        # 划分窗口
        mask_windows = window_partition(img_mask, self.window_size)  # [nW, Mh, Mw, 1]
        # 展平window
        mask_windows = mask_windows.view(-1, self.window_size * self.window_size)  # [nW, Mh*Mw]
        # boardcast广播机制 - window内部当前mask编号，和相同的mask编号位置元素相减为0
        # 只计算其attention，其余置为100，sotfmax后为0
        attn_mask = mask_windows.unsqueeze(1) - mask_windows.unsqueeze(2)  # [nW, 1, Mh*Mw] - [nW, Mh*Mw, 1]  
        # [nW, Mh*Mw, Mh*Mw]  masked_fill 非0的位置设置为-100
        attn_mask = attn_mask.masked_fill(attn_mask != 0, float(-100.0)).masked_fill(attn_mask == 0, float(0.0))
        return attn_mask

    # 输入x时，记录了图片的H，W，传递至blk，后续block中会aserrt L==H*W
    def forward(self, x, H, W):
        # create_mask 若为SW-MSA时 创建蒙板
        # 根据不同尺寸的x,可以重新生成mask蒙板，解决多尺度问题
        # (区别于源码，根据输入直接固定了mask，若后续尺寸不一样会导致报错)
        attn_mask = self.create_mask(x, H, W)  # [nW, Mh*Mw, Mh*Mw]
        for blk in self.blocks:
            # 传递了H，W
            blk.H, blk.W = H, W
            # 默认不会使用if内的程序
            if not torch.jit.is_scripting() and self.use_checkpoint:
                x = checkpoint.checkpoint(blk, x, attn_mask)
            else:
                # 直接执行，迭代更新，上一个block的输出是下一个block的输入
                x = blk(x, attn_mask)
                
        if self.downsample is not None:
            x = self.downsample(x, H, W)
            # 若为奇数的话，h=(h+1)/2
            # 若为偶数的话，w=w/2
            H, W = (H + 1) // 2, (W + 1) // 2

        return x, H, W


class SwinTransformer(nn.Module):
    r""" Swin Transformer
        A PyTorch impl of : `Swin Transformer: Hierarchical Vision Transformer using Shifted Windows`  -
          https://arxiv.org/pdf/2103.14030

    Args:
        patch_size (int | tuple(int)): Patch size. Default: 4
        in_chans (int): Number of input image channels. Default: 3
        num_classes (int): Number of classes for classification head. Default: 1000
        embed_dim (int): Patch embedding dimension. Default: 96
        depths (tuple(int)): Depth of each Swin Transformer layer.
        num_heads (tuple(int)): Number of attention heads in different layers.
        window_size (int): Window size. Default: 7
        mlp_ratio (float): Ratio of mlp hidden dim to embedding dim. Default: 4
        qkv_bias (bool): If True, add a learnable bias to query, key, value. Default: True
        drop_rate (float): Dropout rate. Default: 0
        attn_drop_rate (float): Attention dropout rate. Default: 0
        drop_path_rate (float): Stochastic depth rate. Default: 0.1
        norm_layer (nn.Module): Normalization layer. Default: nn.LayerNorm.
        patch_norm (bool): If True, add normalization after patch embedding. Default: True
        use_checkpoint (bool): Whether to use checkpointing to save memory. Default: False
    """

    def __init__(self, patch_size=4, in_chans=3, num_classes=1000,
                 embed_dim=96, depths=(2, 2, 6, 2), num_heads=(3, 6, 12, 24),
                 # mlp_ratio 第一个全连接层是翻多少倍
                 window_size=7, mlp_ratio=4., qkv_bias=True,
                 # attn_drop_rate  self-attention的drop_rate
                 # drop_path_rate  线性递增至0.1
                 drop_rate=0., attn_drop_rate=0., drop_path_rate=0.1,
                 norm_layer=nn.LayerNorm, patch_norm=True,
                 use_checkpoint=False, **kwargs):
        super().__init__()

        self.num_classes = num_classes
        self.num_layers = len(depths)  # stage的个数
        self.embed_dim = embed_dim
        self.patch_norm = patch_norm
        # stage4输出特征矩阵的channels
        self.num_features = int(embed_dim * 2 ** (self.num_layers - 1)) # embed_dim*2^(lay_nums-1)
        self.mlp_ratio = mlp_ratio

        # split image into non-overlapping patches
        self.patch_embed = PatchEmbed(
            patch_size=patch_size, in_c=in_chans, embed_dim=embed_dim,
            norm_layer=norm_layer if self.patch_norm else None)
        self.pos_drop = nn.Dropout(p=drop_rate)

        # stochastic depth
        # depth 2/2/6/2 sum=12 swin-transformer的block个数
        dpr = [x.item() for x in torch.linspace(0, drop_path_rate, sum(depths))]  # stochastic depth decay rule

        # build layers
        self.layers = nn.ModuleList()
        
        # num_layers = stage个数
        for i_layer in range(self.num_layers):
            # 注意这里构建的stage和论文图中有些差异
            # 这里的stage不包含该stage的patch_merging层，包含的是下个stage的
            layers = BasicLayer(dim=int(embed_dim * 2 ** i_layer),  # 初始dim等于embed_dim,经过一个lays，dim*2
                                depth=depths[i_layer],
                                num_heads=num_heads[i_layer],
                                window_size=window_size,
                                mlp_ratio=self.mlp_ratio,
                                qkv_bias=qkv_bias,
                                # dropout rate
                                drop=drop_rate,
                                # attention的dropout_rate
                                attn_drop=attn_drop_rate,
                                # 随机深度模块的rate取值
                                drop_path=dpr[sum(depths[:i_layer]):sum(depths[:i_layer + 1])],
                                norm_layer=norm_layer,
                                # 对于stage4，不存在PatchMerging的操作
                                downsample=PatchMerging if (i_layer < self.num_layers - 1) else None,
                                use_checkpoint=use_checkpoint)
            self.layers.append(layers)

        self.norm = norm_layer(self.num_features)
        # AdaptiveAvgPool1d自适应平均池化层，指定输出的H，W的大小即可
        self.avgpool = nn.AdaptiveAvgPool1d(1)
        self.head = nn.Linear(self.num_features, num_classes) if num_classes > 0 else nn.Identity()

        self.apply(self._init_weights)

    def _init_weights(self, m):
        if isinstance(m, nn.Linear):
            nn.init.trunc_normal_(m.weight, std=.02)
            if isinstance(m, nn.Linear) and m.bias is not None:
                nn.init.constant_(m.bias, 0)
        elif isinstance(m, nn.LayerNorm):
            nn.init.constant_(m.bias, 0)
            nn.init.constant_(m.weight, 1.0)

    def forward(self, x):
        # x: [B, L, C]
        x, H, W = self.patch_embed(x)
        # 随机丢弃一定的输入，置为0
        x = self.pos_drop(x)

        for layer in self.layers:  # 每个layers代表一个stage，stage对应多个block
            x, H, W = layer(x, H, W)  # 更新迭代

        # 此时x为stage4的输出
        x = self.norm(x)  # [B, L, C]
        x = self.avgpool(x.transpose(1, 2))  # [B, C, 1]
        x = torch.flatten(x, 1)  # 展平了最后一个维度，[B,C]
        x = self.head(x)  # [B,C] -> [B,num_class] C表示num_features
        return x


